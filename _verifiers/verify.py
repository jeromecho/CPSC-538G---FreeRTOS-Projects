def verify_gedf_trace(log_text, num_cores=2):
    IDLE_DEADLINE = 4294967295

    # 1. Parse and filter lines
    lines = [
        line
        for line in log_text.strip().split("\n")
        if "," in line and not line.startswith("TIMESTAMP")
    ]

    # 2. Group events by TIMESTAMP
    # We use a list to preserve the order of events within the same tick
    ticks = collections.defaultdict(list)
    for entry in lines:
        parts = entry.split(",")
        try:
            # Grouping by the first column (TIMESTAMP)
            tick_val = int(parts[0])
            ticks[tick_val].append(parts)
        except ValueError:
            continue

    ready_queue = {}  # task_id -> deadline
    running_on_core = {}  # core_id -> (task_id, deadline)
    violations = []

    # Sort ticks numerically
    for t in sorted(ticks.keys()):
        events = ticks[t]

        # Sort events within the tick by CORE_SEQ (parts[4]) to ensure correct state transitions
        events.sort(key=lambda x: int(x[4]))

        # --- PHASE 1: Update State Machine for the entire tick ---
        for parts in events:
            event_type = parts[1]
            core = parts[3]
            task_id = parts[6]
            deadline = int(parts[13])

            if deadline == IDLE_DEADLINE:
                # If an idle task is being replaced or stopped
                if (
                    event_type == "2"
                    and core in running_on_core
                    and running_on_core[core][0] == task_id
                ):
                    del running_on_core[core]
                continue

            if event_type == "0":  # Arrival
                ready_queue[task_id] = deadline
            elif event_type == "1":  # Run
                running_on_core[core] = (task_id, deadline)
                if task_id in ready_queue:
                    del ready_queue[task_id]
            elif event_type == "2":  # Stop/Preempt
                if core in running_on_core and running_on_core[core][0] == task_id:
                    del running_on_core[core]
                if task_id in ready_queue:
                    del ready_queue[task_id]

        # --- PHASE 2: Post-Tick Formal Verification ---
        # Now that all events for tick 't' have processed, check the "settled" state

        # A. Work Conserving Check
        # If there are tasks waiting, all cores must be utilized.
        if ready_queue and len(running_on_core) < num_cores:
            violations.append(
                f"Tick {t}: Work Conserving Violation. "
                f"Ready Tasks: {list(ready_queue.keys())}, "
                f"Cores Busy: {len(running_on_core)}/{num_cores}"
            )

        # B. Priority (EDF) Check
        # The deadline of every running task must be <= the deadline of every ready task.
        if ready_queue and running_on_core:
            earliest_ready_deadline = min(ready_queue.values())
            for core_id, (task_id, run_deadline) in running_on_core.items():
                if run_deadline > earliest_ready_deadline:
                    violations.append(
                        f"Tick {t}: Priority Violation. "
                        f"Core {core_id} running Task {task_id} (D={run_deadline}), "
                        f"but Task with D={earliest_ready_deadline} is in Ready Queue."
                    )

    return violations


